<h1>Diet Principles</h1>
<p>Concerning food, I’m as much an expert as you are. Recently, I read a summary of <a href="https://amzn.to/320yIhj">“Food Rules: An Eater’s Manual”</a> by Michael Pollan. The one memorable sentence from it is:</p>
<blockquote>
<p>Eat food, not too much, mostly plants.</p>
</blockquote>
<h2 id="eat_food">Eat food</h2>
<p>That means: Not food-like substances. Luckily, it’s rather easy to find this kind of food around my office. It’s even easier to get this done at home. Mostly, it’s some falafel, ramen or a mix of beans and veggies at my currently <a href="https://www.appleeve.de">favorite vegan restaurant</a>.</p>
<p>Okay, sometimes, I do eat a Beyond Meat burger, but at least that’s still vegan :smile:</p>
<h2 id="not_too_much">Not too much</h2>
<p>Yeah, working on that. Ice cream doesn’t count, right?</p>
<h2 id="mostly_plants">Mostly plants</h2>
<p>I eat vegan food as much as possible. I don’t go to great lengths to make sure everything is vegan, though. If I can only get a vegetarian option when I’m out, I’m fine with that. At home, though, it’s easy (yes, easy) to stick to a vegan diet.</p>
<p>For breakfast, for example, I prepare a mix for overnight oats about every three weeks:</p>
<ul>
  <li>Oats or spelt as base</li>
  <li>Flax seeds</li>
  <li>Chia seeds</li>
  <li>Psyllium husk (yes, I googled that. Flohsamenschalen in german)</li>
  <li>Grated coconut</li>
  <li>Crushed almonds</li>
  <li>Goji berries</li>
</ul>
<p>Six tablespoons of that in a water glass, some kind of vegan milk on top, some blueberries on top. Done. I don’t think about this anymore. I just eat it and am fine until lunch. Simple.</p>