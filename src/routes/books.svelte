<h2>Man's Search for Meaning</h2>

<p>This book helped a lot with setting things that happen in my life into perspective. Also, I love this bit:</p>

<blockquote><p>Between stimulus and response, there is a space. In that space is our power to choose our response. In our response lies our growth and our freedom.</p></blockquote>

<p><a href="https://amzn.to/2wDxESH">Buy the book on Amazon</a></p>

</article>
