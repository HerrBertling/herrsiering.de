<h2>The Achievment Habit</h2>

<p>Same as with Man's Search for Meaning and The Road Less Traveled, this book is a great read about how to appoach life. I loved the part about being right or wrong:</p>

<blockquote><p>Whenever I find myself challenged to a game of right and wrong, I stop playing. Next time you find yourself playing right and wrong, remember. You give everything in your life its meaning, so you can choose to end the game. It does not matter how right you are or how wrong they are; you lose just by playing.</p></blockquote>

<p><a href="https://amzn.to/2PBFt2Z">Buy the book on Amazon</a></p>

</article>

