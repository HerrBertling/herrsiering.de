<h1>Books I really like</h1>

<article>

<h2>The Road Less Traveled</h2>

<p>A wonderful book about how life works and how you might approach it:</p>

<blockquote><p>Life is difficult. This is a great truth, one of the greatest truths. It is a great truth because once we truly see this truth, we transcend it. Once we truly know that life is difficult-once we truly understand and accept it-then life is no longer difficult. Because once it is accepted, the fact that life is difficult no longer matters.</p></blockquote>

<p>I re-read it about every two years.</p>

<p><a href="https://amzn.to/2Q0WuVf">Buy the book on Amazon</a></p>

